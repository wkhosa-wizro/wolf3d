#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*sjoin;
	size_t	len;

	if (s1 == NULL || s2 == NULL)
		return (NULL);
	len = ft_strlen(s1) + ft_strlen(s2);
	if (!(sjoin = ft_strnew(len)))
		return (NULL);
	ft_strcat(sjoin, s1);
	return (ft_strcat(sjoin, s2));
}
